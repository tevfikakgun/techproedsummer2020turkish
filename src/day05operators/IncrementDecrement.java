package day05operators;

public class IncrementDecrement {

	public static void main(String[] args) {
		// int de variable oluþtur.
		// bunun deðerini çarpma ve bölme kullanarak arttýrýn ve azaltýn
		
		int num1 = 12; // uzun yolla çarpma yaparak
		num1 = num1 * 3; // 36
		System.out.println(num1);
		
		num1*=3;// 36*3=108
		System.out.println(num1);
		
		// bölme ile decrement
	    num1 = num1 / 9;  // 108 / 9= 12
	    System.out.println(num1);
	    
	    num1/=2; //  12/2=6
	    System.out.println(num1);
	    
		
		
		
		

	}

}
