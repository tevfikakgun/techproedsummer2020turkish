package day05operators;

public class Increment04 {

	public static void main(String[] args) {
		// int data type 1 veriable oluştur.
		// Bu variable yi her seferinde 1 artırmak için 3 yöntemi kullanın.
		
		int num1 = 10;
		num1 = num1 + 1;//10+1=11
		num1+=1;// 11+1=12
		num1++; // 11+1=13
		System.out.println(num1);
		

	}

}
