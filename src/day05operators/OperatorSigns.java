package day05operators;

public class OperatorSigns {

	public static void main(String[] args) {
		
		boolean b1 = 12==20;
		System.out.println(b1);// false
		
		System.out.println(12<20);// true
		System.out.println(12!=20);//true
		System.out.println(12>=20);//false
		System.out.println(12<=12);//true
		
		
		
		
		
		

	}

}
