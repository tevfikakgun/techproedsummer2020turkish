package day05operators;

public class Decrement01 {

	public static void main(String[] args) {
		// int data type de bir variable oluþturun.
		// bu veriable in deðerini 3 yöntemide kullanarak bir azaltýn.
		
		int num1 = 12;
		num1 = num1 - 1; // 11
		System.out.println(num1);
		
		num1-=1; // 10
		System.out.println(num1);
		
		num1--; // 9
		System.out.println(num1);// 9
		
		

	}

}
